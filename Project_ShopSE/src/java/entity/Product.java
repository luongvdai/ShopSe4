package entity;

/**
 *
 * @author HP
 */
public class Product {
    private int id;
    private String name;
    private int cid;
    private float price;
    private String description;
    private String image;
    private int number;

    public Product() {
    }

    public Product(int id, String name, int cid, float price, String description, String image, int number) {
        this.id = id;
        this.name = name;
        this.cid = cid;
        this.price = price;
        this.description = description;
        this.image = image;
        this.number = number;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getCid() {
        return cid;
    }

    public void setCid(int cid) {
        this.cid = cid;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }
    
}
