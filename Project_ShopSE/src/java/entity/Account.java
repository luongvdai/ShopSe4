package entity;

/**
 *
 * @author HP
 */
public class Account {
    private int id;
    private String user;
    private String password;
    private String name;
    private int manager;

    public Account() {
    }

    public Account(int id, String user, String password, String name, int manager) {
        this.id = id;
        this.user = user;
        this.password = password;
        this.name = name;
        this.manager = manager;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getManager() {
        return manager;
    }

    public void setManager(int manager) {
        this.manager = manager;
    }
    
}
